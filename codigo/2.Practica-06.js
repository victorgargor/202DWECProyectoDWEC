function seleccionMenu(menu) {
    switch(menu){
        case "Menu 1":
            alert("Has elegido el Menú 1.");
        break;
        case "Menu 2":
            alert("Has elegido el Menú 2.");
        break;
        case "Menu 3":
            alert("Has elegido el Menú 3.");
        break;
        default:
            alert("Por el momento no disponemos de más menús. Por favor, elige 1, 2 o 3.");
        break;
    }
}    